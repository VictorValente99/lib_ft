#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	char	*new_str;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1) - 1;
	while (start <= end && ft_strchr(set, s1[start]))
		start++;
	if (start > end)
		return (ft_strdup(""));
	while (ft_strchr(set, s1[end]) && end >= 0)
		end--;
	new_str = (char *)ft_calloc(end - start + 2, sizeof(char));
	if (!new_str)
		return (NULL);
	ft_strlcpy(new_str, &s1[start], end - start + 2);
	return (new_str);
}
